    private String[] obtenerLugares(){
        ArrayList<AID> lugaresAid = miAgente.getLugares();
        String[] lista = new String[lugaresAid.size()];
        for(int i=0; i<lugaresAid.size(); i++){
            lista[i]= lugaresAid.get(i).getLocalName();
        }
        return lista;
    }
    private String[] obtenerTransportes(){
        ArrayList<AID> transportesAid = miAgente.getTransportes();
        String[] lista = new String[transportesAid.size()];
        for(int i=0; i<transportesAid.size(); i++){
            lista[i]= transportesAid.get(i).getLocalName();
        }
        return lista;
    }

